function renderAdmin(section) {
  if (S.user?.role !== 'admin') { go('home'); return ''; }

  const totalExams    = EXAMS.length;
  const totalQ        = QUESTIONS.length;
  const totalUsers    = LS.users().length;
  const totalResults  = S.results.length;

  const sidebar = ['dashboard','exams','questions','users','results'].map(s => {
    const labels = { dashboard:'📊 الإحصائيات', exams:'📝 الامتحانات', questions:'❓ الأسئلة', users:'👤 المستخدمون', results:'📋 النتائج' };
    return `<div class="sidebar-item ${section===s?'active':''}" onclick="go('admin/${s}')">${labels[s]}</div>`;
  }).join('');

  let content = '';
  if (section === 'dashboard') {
    content = `
    <div class="admin-section-title">📊 إحصائيات الموقع</div>
    <div class="grid grid-4" style="gap:12px;margin-bottom:24px">
      ${[['📝','الامتحانات',totalExams],['❓','الأسئلة',totalQ],['👤','المستخدمون',totalUsers],['📋','النتائج',totalResults]]
        .map(([ic,lb,v]) => `<div class="card card-body" style="text-align:center">
          <div style="font-size:28px">${ic}</div>
          <div style="font-size:28px;font-weight:800;color:var(--primary)">${v}</div>
          <div class="text-sm text-muted">${lb}</div>
        </div>`).join('')}
    </div>
    <div class="card card-body">
      <div style="font-size:15px;font-weight:700;margin-bottom:16px">التخصصات والمواد</div>
      ${SPECS.map(s => {
        const subs = where(SUBJECTS,'specId',s.id);
        return `<div style="display:flex;align-items:center;gap:12px;padding:10px 0;border-bottom:1px solid var(--border-light)">
          <span style="font-size:22px">${s.icon}</span>
          <div><div style="font-weight:700">${s.name}</div><div class="text-sm text-muted">${subs.length} مواد</div></div>
        </div>`;
      }).join('')}
    </div>`;
  }
  else if (section === 'exams') {
    content = `
    <div class="admin-section-title">📝 الامتحانات (${EXAMS.length})</div>
    <div class="card" style="overflow-x:auto">
      <table class="admin-table">
        <thead><tr><th>#</th><th>الامتحان</th><th>الوحدة</th><th>الأسئلة</th><th>الوقت</th><th>الحالة</th></tr></thead>
        <tbody>
          ${EXAMS.map(e => {
            const unit = byId(UNITS, e.unitId);
            const qc   = where(QUESTIONS,'examId',e.id).length;
            return `<tr>
              <td>${e.id}</td>
              <td><strong>${e.title}</strong></td>
              <td>${unit?.name||'-'}</td>
              <td>${qc}</td>
              <td>${e.duration} دقيقة</td>
              <td>${e.isFree ? '<span class="tag tag-green">مجاني</span>' : '<span class="tag tag-gray">مقفل</span>'}</td>
            </tr>`;
          }).join('')}
        </tbody>
      </table>
    </div>`;
  }
  else if (section === 'questions') {
    content = `
    <div class="admin-section-title">❓ الأسئلة (${QUESTIONS.length})</div>
    <div class="card" style="overflow-x:auto">
      <table class="admin-table">
        <thead><tr><th>#</th><th>السؤال</th><th>الامتحان</th><th>الإجابة الصحيحة</th></tr></thead>
        <tbody>
          ${QUESTIONS.slice(0,50).map(q => {
            const exam = byId(EXAMS, q.examId);
            const labels = ['أ','ب','ج','د'];
            return `<tr>
              <td>${q.id}</td>
              <td style="max-width:300px;white-space:nowrap;overflow:hidden;text-overflow:ellipsis">${q.text}</td>
              <td class="text-sm">${exam?.title?.substring(0,30)||'-'}...</td>
              <td><span class="tag tag-green">${labels[q.ans]}) ${q.opts[q.ans].substring(0,30)}</span></td>
            </tr>`;
          }).join('')}
          ${QUESTIONS.length > 50 ? `<tr><td colspan="4" class="text-center text-muted text-sm">... و ${QUESTIONS.length-50} سؤال آخر</td></tr>` : ''}
        </tbody>
      </table>
    </div>`;
  }
  else if (section === 'users') {
    const users = LS.users();
    content = `
    <div class="admin-section-title">👤 المستخدمون المسجلون (${users.length})</div>
    ${users.length === 0
      ? `<div class="empty-state"><div class="empty-icon">👤</div><div class="empty-title">لا يوجد مستخدمون مسجلون بعد</div><div class="empty-desc">المستخدمون الذين يسجلون بالبريد الإلكتروني يظهرون هنا</div></div>`
      : `<div class="card" style="overflow-x:auto">
          <table class="admin-table">
            <thead><tr><th>الاسم</th><th>البريد</th><th>الاشتراك</th><th>إجراء</th></tr></thead>
            <tbody>
              ${users.map((u,i) => `<tr>
                <td>${u.name}</td>
                <td class="text-sm">${u.email}</td>
                <td>${u.subscribed ? '<span class="tag tag-green">مشترك ✓</span>' : '<span class="tag tag-gray">غير مشترك</span>'}</td>
                <td>
                  <button class="btn btn-sm ${u.subscribed?'btn-destructive':'btn-outline'}" onclick="toggleUserSub(${i},'admin')">
                    ${u.subscribed ? 'إلغاء الاشتراك' : 'تفعيل الاشتراك'}
                  </button>
                </td>
              </tr>`).join('')}
            </tbody>
          </table>
        </div>`}`;
  }
  else if (section === 'results') {
    content = `
    <div class="admin-section-title">📋 النتائج (${S.results.length})</div>
    ${S.results.length === 0
      ? `<div class="empty-state"><div class="empty-icon">📋</div><div class="empty-title">لا توجد نتائج بعد</div></div>`
      : `<div class="card" style="overflow-x:auto">
          <table class="admin-table">
            <thead><tr><th>الامتحان</th><th>النتيجة</th><th>النسبة</th><th>التاريخ</th></tr></thead>
            <tbody>
              ${[...S.results].sort((a,b)=>b.date-a.date).map(r => {
                const exam = byId(EXAMS, r.examId);
                const pct  = Math.round(r.score/r.total*100);
                const date = new Date(r.date).toLocaleDateString('ar-JO');
                return `<tr>
                  <td>${exam?.title||'امتحان'}</td>
                  <td>${r.score}/${r.total}</td>
                  <td><span class="score-chip ${scoreClass(pct)}" style="padding:3px 10px">${pct}%</span></td>
                  <td class="text-sm text-muted">${date}</td>
                </tr>`;
              }).join('')}
            </tbody>
          </table>
        </div>`}`;
  }

  return `
  ${header()}
  <div style="padding-top:64px">
    <div class="admin-layout">
      <div class="admin-sidebar">
        <div class="sidebar-title">لوحة التحكم</div>
        ${sidebar}
      </div>
      <div class="admin-content">
        ${content}
      </div>
    </div>
  </div>`;
}

function toggleUserSub(idx, section) {
  const users = LS.users();
  users[idx].subscribed = !users[idx].subscribed;
  LS.saveUsers(users);
  toast(users[idx].subscribed ? `✅ تم تفعيل اشتراك ${users[idx].name}` : `تم إلغاء اشتراك ${users[idx].name}`);
  document.getElementById('app').innerHTML = renderAdmin(section);
}

