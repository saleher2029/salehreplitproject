function go(hash) { location.hash = hash; }

function route() {
  const hash   = location.hash.replace('#','') || '';
  const parts  = hash.split('/');
  const page   = parts[0] || '';

  if (!S.user && page !== 'login') { go('login'); return; }
  if (S.user  && page === 'login') { go('home');  return; }

  const app = document.getElementById('app');

  if (page === 'login')          { app.innerHTML = renderLogin(); }
  else if (page === 'home')      { app.innerHTML = renderHome(); }
  else if (page === 'spec')      { app.innerHTML = renderSubjects(+parts[1]); }
  else if (page === 'subject')   { app.innerHTML = renderUnits(+parts[1], +parts[2]); }
  else if (page === 'unit')      { app.innerHTML = renderExams(+parts[1], +parts[2], +parts[3]); }
  else if (page === 'exam')      { renderTakeExam(+parts[1]); }
  else if (page === 'result')    { app.innerHTML = renderResult(+parts[1]); }
  else if (page === 'my-exams')  { app.innerHTML = renderMyExams(); }
  else if (page === 'admin')     { app.innerHTML = renderAdmin(parts[1] || 'dashboard'); }
  else                           { go(S.user ? 'home' : 'login'); }

  window.scrollTo(0, 0);
}

window.addEventListener('hashchange', () => {
  stopTimer();
  route();
});

