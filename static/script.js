function startTimer() {
  stopTimer();
  S.timer = setInterval(() => {
    if (!S.exam) { stopTimer(); return; }
    S.exam.remaining--;
    const el = document.getElementById('timer-display');
    if (el) {
      el.textContent = fmt(S.exam.remaining);
      el.className = 'time' + (S.exam.remaining < 120 ? ' danger' : S.exam.remaining < 300 ? ' warning' : '');
    }
    if (S.exam.remaining <= 0) {
      stopTimer();
      toast('انتهى الوقت! جاري التسليم...', 'warning');
      setTimeout(submitExam, 1000);
    }
  }, 1000);
}

function stopTimer() {
  if (S.timer) { clearInterval(S.timer); S.timer = null; }
}

function selectOption(qId, idx) {
  if (!S.exam) return;
  S.exam.answers[qId] = idx;
  // Re-render options
  const q = S.exam.questions[S.exam.current];
  if (q.id !== qId) return;
  document.querySelectorAll('.option-btn').forEach((btn, i) => {
    btn.classList.toggle('selected', i === idx);
  });
  // Update nav
  refreshNav();
  // Update answered count
  const el = document.querySelector('.tag.tag-gray');
  if (el) el.textContent = `${Object.keys(S.exam.answers).length}/${S.exam.questions.length} مجاب`;
}

function toggleFlag(qId) {
  if (!S.exam) return;
  S.exam.flags[qId] = !S.exam.flags[qId];
  const btn = document.querySelector('.q-flag-btn');
  if (btn) { btn.classList.toggle('flagged', S.exam.flags[qId]); btn.textContent = S.exam.flags[qId] ? '🚩' : '⚑'; }
  refreshNav();
}

function refreshNav() {
  const nav = document.getElementById('q-nav');
  if (!nav || !S.exam) return;
  nav.innerHTML = S.exam.questions.map((q,i) => {
    let cls = '';
    if (i === S.exam.current) cls = 'current';
    else if (S.exam.flags[q.id]) cls = 'flagged';
    else if (S.exam.answers[q.id] !== undefined) cls = 'answered';
    return `<button class="qnav-btn ${cls}" onclick="jumpTo(${i})">${i+1}</button>`;
  }).join('');
}

function jumpTo(i) {
  if (!S.exam || i < 0 || i >= S.exam.questions.length) return;
  S.exam.current = i;
  const exam = byId(EXAMS, S.exam.examId);
  document.getElementById('app').innerHTML = buildExamUI(exam, S.exam.questions);
  startTimer();
}

function nextQ() {
  if (!S.exam || S.exam.current >= S.exam.questions.length - 1) return;
  S.exam.current++;
  const exam = byId(EXAMS, S.exam.examId);
  document.getElementById('app').innerHTML = buildExamUI(exam, S.exam.questions);
  startTimer();
}

function prevQ() {
  if (!S.exam || S.exam.current <= 0) return;
  S.exam.current--;
  const exam = byId(EXAMS, S.exam.examId);
  document.getElementById('app').innerHTML = buildExamUI(exam, S.exam.questions);
  startTimer();
}

function confirmSubmit() {
  document.getElementById('submit-modal')?.classList.remove('hidden');
}

function submitExam() {
  if (!S.exam) return;
  stopTimer();
  const { examId, questions, answers } = S.exam;
  let score = 0;
  const details = questions.map(q => {
    const selected = answers[q.id];
    const correct  = q.ans;
    if (selected === correct) score++;
    return { qId:q.id, selected, correct };
  });

  const result = {
    id:     Date.now(),
    examId,
    score,
    total:  questions.length,
    date:   Date.now(),
    details,
    duration: byId(EXAMS, examId)?.duration * 60 - S.exam.remaining,
  };

  S.results.push(result);
  LS.saveResults();
  S.exam = null;
  go(`result/${result.id}`);
}

